int		ft_atoi(const char *str)
{
	int	i;
	int ret;

	i = 0;
	ret = 0;
	while (str[i] && !(str[i] >= '1' && str[i] <= '9'))
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		ret *= 10;
		ret += str[i] - '0';
		i++;
	}
	return (ret);
}
